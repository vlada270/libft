#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start,
		size_t len)
{
	unsigned int		i;
	char				*str;

	if (!(str = (char*)malloc(sizeof(*str) * (len + 1))))
		return (NULL);
	if (s == 0)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s[i + start];
		i++;
	}
	str[i] = '\0';
	return (str);
}
